        	<p><strong>CIKARANG UTARA</strong> - Harga kebutuhan pokok di Pasar Baru Cikarang Kabupaten Bekasi secara umum terpantau stabil. Berikut ini daftar harga komoditas yang dihimpun UPTD Pengelolaan Pasar Baru Cikarang dan Pertokoan Cikarang pada Kamis, 17 November 2022.&nbsp;</p>
<p>Harga cabai keriting merah Rp 30.000/kg, cabai keriting hijau Rp 20.000/kg, cabai merah besar Rp 35.000/kg, cabai hijau besar Rp 25.000/kg, cabai rawit merah Rp 40.000/kg, cabai rawit hijau Rp 20.000/kg.&nbsp;</p>
<p>Bawang putih Rp 25.000/kg, bawang merah Rp 30.000/kg. Tomat Rp 16.000/kg, wortel Rp 10.000/kg, kol Rp 10.000/kg, dan kentang Rp 17.000/kg.</p>
<p>Minyak goreng curah Rp 15.200/kg, minyak goreng kemasan sederhana Rp 24.000/kg, minyak goreng bimoli Rp 24.000/kg.&nbsp;</p>
<p>Kemudian gula putih Rp 14.500/kg, gula merah Rp 35.000/kg. Tepung terigu segitiga biru Rp 10.000/kg, tepung curah Rp 7000/kg.&nbsp;</p>
<p>Kacang tanah Rp 32.000/kg, kacang hijau Rp 25.000/kg. Ubi Rp 5000/kg dan singkong Rp 3000/kg. Tempe Rp 5000/kg, tahu Rp 5000/kg.</p>
<p>Daging sapi beku Rp 90.000/kg, daging sapi paha depan Rp 135.000/kg, daging sapi paha belakang Rp 135.000/kg, daging sapi sandung lamur Rp 50.000/kg, daging sapi tetelan Rp 40.000/kg.&nbsp;</p>
<p>Daging ayam ras Rp 28.000/kg, daging ayam kampung Rp 75.000/kg. Daging kambing Rp 110.000/kg. Telur ayam ras Rp 28.000/kg, telur ayam kampung Rp 2.500/butir. Telur bebek Rp 3.000/butir.&nbsp;</p>
<p>Selanjutnya, ikan mas Rp 28.000/kg, ikan lele Rp 24.000/kg, ikan bandeng Rp 35.000/kg, ikan kakap Rp 75.000/kg, ikan kembung Rp 48.000//kg, ikan tongkol Rp 42.000/kg. Ikan asin teri medan Rp 160.000/kg.</p>
<p>Jagung pipilan kuning Rp 9.000/kg, jagung manis Rp 10.000/kg. Kedelai lokal Rp 10.000/kg, kedelai impor Rp 14.000/kg.&nbsp;&nbsp;</p>
<p>Beras IR 1 Rp 11.500/kg, IR 2 Rp 11.000/kg, IR 42 Rp 10.500/kg, beras muncul Rp 10.000/kg, beras pandan wangi Rp 12.000/kg, beras ramos Rp 11.500/kg, beras ketan putih Rp 12.000/kg dan beras ketan hitam Rp 22.000/kg. (Syahnaz Alfi Syahrina)&nbsp;</p>
<p>Editor : Yus Ismail</p>
